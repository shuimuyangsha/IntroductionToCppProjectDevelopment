#pragma once
#include "IMark.h"

/*
机器人，激光在窗口上留下的痕迹
*/

class CShooter3Mark :
    public IMark
{
public:
    CShooter3Mark(float x, float y);
    virtual ~CShooter3Mark();
    // 画自己
    virtual void Draw(Gdiplus::Graphics &gh);
    // 状态是否还在变化
    virtual bool IsChanging() const;

private:
    // 固定图片
    Image *m_img;
    // 图片的角度
    float m_degree;
    // 动态下落的东西
    typedef struct {
        Image *m_img2;
        // 位置
        PointF m_pos;
        // 速度(包含方向)
        PointF m_speed{ 10.0f, 15.0f };
        // 当前角度(用于自射旋转)
        float m_dir{ 0.0f };
        // 是否显示
        bool m_bShow{ true };
    } SDynamic;
    std::vector<SDynamic> m_vD;
};

